let currentLang = localStorage.getItem('lang') || 'ko';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);

  // .nav-ko/.nav-en, .brand-ko/.brand-en 직접 토글
  const isKo = (lang === 'ko');
  document.documentElement.lang = isKo ? 'ko' : 'en';
  document.querySelectorAll('.nav-ko, .brand-ko').forEach(el => el.style.display = isKo ? '' : 'none');
  document.querySelectorAll('.nav-en, .brand-en').forEach(el => el.style.display = isKo ? 'none' : '');

  applyTranslations();

  document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
  const btn = document.getElementById('btn-' + lang);
  if (btn) btn.classList.add('active');
  document.documentElement.setAttribute('data-lang', lang);
}

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) ||
         (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || null;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== null) el.innerHTML = val;
    // 번역 없으면 HTML에 작성된 기본값 그대로 유지
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key);
    if (val !== null) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = t(key);
    if (val !== null) el.title = val;
  });
  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria-label');
    const val = t(key);
    if (val !== null) el.setAttribute('aria-label', val);
  });
}

/** Inline confirm(): use onsubmit="return confirmI18n('key')" */
function confirmI18n(key) {
  const v = t(key);
  return window.confirm(v != null ? v : key);
}
window.confirmI18n = confirmI18n;

document.addEventListener('DOMContentLoaded', () => {
  setLang(currentLang);
});
